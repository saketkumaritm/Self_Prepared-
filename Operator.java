class Operator {
    public static void main(String[] args) {

        // Airthematic operator
        int a = 9;
        int b = a + 5;
        System.out.println(b);

        // // Assigning Operator
        int c = 3;
        c += 4;
        System.out.println(c);

        // //Comperision operator
        System.out.println(65 < 5);

        // Logical operator
        System.out.println(65 > 5 && 75 < 85);
        System.out.println(65 > 5 || 71 < 2);

    }
}